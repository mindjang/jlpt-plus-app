/**
 * 시간 관련 상수 정의
 * 일관된 시간 단위 변환을 위한 상수 모음
 */

/**
 * 1시간을 분으로 환산
 */
export const ONE_HOUR_IN_MINUTES = 60

/**
 * 1일을 분으로 환산
 */
export const ONE_DAY_IN_MINUTES = 24 * 60

/**
 * 1주일을 분으로 환산
 */
export const ONE_WEEK_IN_MINUTES = 7 * ONE_DAY_IN_MINUTES

/**
 * 1개월을 분으로 환산 (30일 기준)
 */
export const ONE_MONTH_IN_MINUTES = 30 * ONE_DAY_IN_MINUTES

/**
 * 1년을 분으로 환산 (365일 기준)
 */
export const ONE_YEAR_IN_MINUTES = 365 * ONE_DAY_IN_MINUTES

